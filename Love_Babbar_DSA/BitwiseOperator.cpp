#include <bits/stdc++.h>
using namespace std;

int main(int argc, char const *argv[])
{
    int a = 2;
    int b = 5;

    cout << " & " << (a & b) << endl;
    cout << " | " << (a | b) << endl;
    cout << " ^ " << (a ^ b) << endl;
    cout << " ~a " << ~a << endl;
    cout << " ~b " << ~b << endl;
    return 0;
}